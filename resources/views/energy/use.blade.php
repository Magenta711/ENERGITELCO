<h5 class="title"><B>Tiempo de Uso</B></h5>
<p>Ingresa la cantidad de horas que usas los aparatos diariamente. Para esto ten en cuenta los siguientes puntos:</p>
<ul>
    <li>Si el tiempo de uso corresponde a minutos, ingresar la cantidad como decimal. Ej: 0.5 = 30 minutos</li>
    <li>Si el aparato está siempre en funcionamiento, poner 12 en horas diurnas y 12 en horas nocturnas.</li>
    <li>Si el aparato es recargable, poner la cantidad de horas en las que se carga.</li>
</ul>
